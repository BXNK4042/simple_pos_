// Fallback chain: requested lang -> en -> raw key. A missing translation never
// crashes; it shows the key so the gap is obvious during review.
export function t(lang: Lang, key: string): string {
  return dictionaries[lang]?.[key] ?? dictionaries.en[key] ?? key
}

// Server-side: read the lang cookie in RSCs / route handlers.
export async function getLang(): Promise<Lang> {
  const cookieStore = await cookies()
  const value = cookieStore.get(LANG_COOKIE)?.value
  return isLang(value) ? value : DEFAULT_LANG
}

// Client-side: read the lang cookie fresh (no React state). For use inside
// effects/callbacks where the lang from context would be a stale closure.
export function readLangCookie(): Lang {
  if (typeof document === "undefined") return DEFAULT_LANG
  const match = document.cookie.match(/(?:^|;\s*)pos_lang=([^;]+)/)
  const value = match?.[1]
  return isLang(value) ? value : DEFAULT_LANG
}
